package com.santander.models;

public class Temperature {
	private Number temperature;
	private Number beerQuantity;
	private Number beersBoxes;
	
	public Number getTemperature() {
		return temperature;
	}
	public void setTemperature(Number temperature) {
		this.temperature = temperature;
	}
	public Number getBeerQuantity() {
		return beerQuantity;
	}
	public void setBeerQuantity(Number beerQuantity) {
		this.beerQuantity = beerQuantity;
	}
	public Number getBeersBoxes() {
		return beersBoxes;
	}
	public void setBeersBoxes(Number beersBoxes) {
		this.beersBoxes = beersBoxes;
	}
}
